#include "Block.h"



Block::Block(glm::vec3 pos, glm::vec3 dim, BlockType type) : position_(pos), type_(type) {
	quadModel_ = genQuadModel(dim, type);
}


Block::~Block() {

}

void Block::draw() {
	quadModel_.bindVAO();

	Texture texture(blockTypeToTextFile(type_));
	texture.bind();

	glDrawArrays(GL_TRIANGLES, 0, 36);
}

